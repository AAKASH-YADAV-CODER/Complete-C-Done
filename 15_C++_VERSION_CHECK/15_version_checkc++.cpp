#include <bits/stdc++.h>
using namespace std;

int main()
{
    switch (__cplusplus)
    {
    case 202002L:
        cout << "C++20" << endl;
        break;
    case 201703L:
        cout << "C++17" << endl;
        break;
    case 201402L:
        cout << "C++14" << endl;
        break;
    case 201103L:
        cout << "C++11" << endl;
        break;
    case 199711L:
        cout << "C++98" << endl;
        break;
    default:
        cout << "pre-Standard C++" << endl;
    }
    return 0;
}

